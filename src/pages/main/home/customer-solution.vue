         * 获取用户点击进入的对应的解决方案内容方法
         */
        getSolutionContent () {
            this.solutionIds[0] = 'businessPlan'
            this.solutionIds[1] = 'financialAnalysis'
            this.solutionIds[2] = 'interimAnalysis'
            this.solutionIds[3] = 'projectFeasibility'
            this.solutionIds[4] = 'businessRisk'
            this.solutionIds[5] = 'interimAssessment'
            this.solutionIds[6] = 'overseasProject'
            this.solutionIds[7] = 'allIndustry'
            this.solutionIds[8] = 'environmentalAssessment'
            this.solutionIds[9] = 'socialResponsibility'
            this.solutionIds[10] = 'otherSolutions'
            var catalog = document.getElementById('catalog')
            var catalogLi = catalog.getElementsByTagName('li')
            var detailContent = document.getElementById('detailContent')
            var catalogDetail = detailContent.getElementsByClassName('catalog-detail')
            for (var i = 0; i < 11; i++) {
                if (this.solutionId === this.solutionIds[i]) {
                    catalogLi[i].classList.add('catalog-list-active')
                    catalogDetail[i].classList.remove('hide')
                }
            }
        },
        /**
         * 点击左侧目录改变样式和右侧内容方法
         */
        catalogChange () {
            var catalog = document.getElementById('catalog')
            var catalogLi = catalog.getElementsByTagName('li')
            var detailContent = document.getElementById('detailContent')
            var catalogDetail = detailContent.getElementsByClassName('catalog-detail')
            for (var i = 0; i < catalogLi.length; i++) {
                (function (num) {
                    catalogLi[num].onclick = () => {
                        for (var j = 0; j < catalogLi.length; j++) {
                            (function (temp) {
                                catalogLi[temp].classList.remove('catalog-list-active')
                                catalogDetail[temp].classList.add('hide')
                            })(j)
                        }
                        catalogLi[num].classList.add('catalog-list-active')
                        catalogDetail[num].classList.remove('hide')
                    }
                })(i)
            }
        }
    }
}
