<script>
import DialogMask from '../../../dialog/mask'
import DialogRequirement from '../../../dialog/requirement'
export default {
    name: 'MonitorServiceCenter',
    components: {
        DialogMask,
        DialogRequirement
    },
    data () {
        return {
            /**
             * 是否显示对话弹框遮罩层判断值
             */
            isShowMask: false,
            /**
             * 是否显示提交需求弹框判断值
             */
            isShowRequirement: false
        }
    },
    mounted () {
        this.headerchange()
    },
    methods: {
        /**
         * 显示提交需求弹框方法
         */
        showRequirement () {
            this.isShowMask = true
            this.isShowRequirement = true
        },
        /**
         * 隐藏提交需求弹框方法
         * 作用于子组件DialogRequirement
         */
        hideRequirement () {
            this.isShowMask = false
            this.isShowRequirement = false
        },
        /**
         * header标签变换content内容方法
         */
        headerchange () {
            var headerBox = document.getElementById('header-box') // 标题标签盒子容器
            var headerLi = headerBox.getElementsByTagName('li') // 标题标签
            var contentBox = document.getElementById('content-box') // 标题对应内容盒子容器
            var content = contentBox.getElementsByClassName('content') // 标题对应内容
            var contentArrow = contentBox.getElementsByClassName('content-arrow')[0] // 指向标题标签箭头
            for (var i = 0; i < headerLi.length; i++) {
                (function (num) {
                    headerLi[num].onclick = () => {
                        for (var j = 0; j < headerLi.length; j++) {
                            (function (temp) {
                                headerLi[temp].classList.remove('header-active')
                                content[temp].classList.add('hide')
                            })(j)
                        }
                        headerLi[num].classList.add('header-active') // 给标题标签添加鼠标hover样式
                        content[num].classList.remove('hide') // 去除标题标签对应内容的隐藏样式
                        contentArrow.style.left = 90 + num * 230 + 'px' // 改变指向标题标签的箭头left位置
                    }
                })(i)
            }
        }
    }
}
</script>

